# Here Using the Numpy for Mathematical Functions

import numpy as np


arr = np.arange(1,17,3)

print(arr)

print("Sum: ", np.sum(arr)) # Sum of the array
print('Mean', np.mean(arr))  # Mean of the Array
print('Median', np.median(arr))   # Median  of the array
print('Standard deviation: ', np.std(arr))
print('Exponential:', np.exp(arr))
